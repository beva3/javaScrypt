/** 
 * La méthode substring() retourne une sous-chaîne 
 * de la chaîne courante, entre un indice de début et un indice de fin.
*/
const me ='Raphael'

console.log(me.substring(0,1)); // R
console.log(me.substring(0,2)); // Ra
console.log(me.substring(1,1)); // void

console.log(me.substring(1,2)); // a

console.log(me.substring(1,3)); // ap